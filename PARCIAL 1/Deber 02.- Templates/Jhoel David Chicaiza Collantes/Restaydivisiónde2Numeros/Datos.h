using namespace std;

template <class TipoDato>
void restar(TipoDato numero1, TipoDato numero2){
	TipoDato resta;
	resta=numero1-numero2;
	cout<<"\nLa resta es: "<<resta;
 
}

template <class TipoDato>
void dividir(TipoDato numero1, TipoDato numero2){
	TipoDato division;
	division=numero1/numero2;
	cout<<"\nLa division es: "<<division;
 
}


